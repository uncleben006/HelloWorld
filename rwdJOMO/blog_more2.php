							<p>透過粉絲專頁「桌末狂歡 Jomor - 桌遊資訊平台」所舉辦為期八天的活動「搶救皮皮君：森林派對の危機」之中(10/8-10/15)，從二十款新天鵝堡派對桌遊中票選出前十名，將分為上下篇，提供讀者在派對遊戲中有更多的選擇。</p>

							<h1>第5名:矮人礦坑SABOTEUR</h1>
							<img src="jomor_html/img/blogimg/blog_title02-1.png" class="blog_gameimg">
							<p>矮人礦坑是一款規則簡單、容易上手的派對陣營遊戲，很適合剛入門的新手玩。</p>
							<p>遊戲目標：好矮人- 挖取金礦；壞矮人- 阻止好矮人挖礦。</p>
							</p>
							<p>
							遊戲中玩家們將要扮演成兩種不同的矮人。其中一種是淘金的好矮人，他們將要開拓坑道去尋找金礦；另一種則是在路上想辦法進行阻礙的壞矮人。自己的身分只有自己知道，大家都不會知道彼此的身分，所以你只能由出牌的過程來猜測誰是好人誰是壞人。需要絞盡腦汁幫助自己的同伴以及想辦法隱藏自己的角色，用心機使用功能牌來陷害敵方陣營或是保護隊友是這款遊戲最大的特點。最後如果好矮人成功的開通了前往金礦的坑道，他們將得到金礦作為獎賞，而壞矮人將什麼都沒有；不過如果好矮人失敗了沒挖到金礦，則是壞矮人得到金礦。
							</p>
							<p>
							每位玩家所扮演的角色(陣營)，只有在最後分金礦的時候才會揭曉。每個回合都會重新抽角色，而遊戲進行三回合後結束獲得最多金礦的玩家就獲勝。這是個可以10個好友一起同樂的派對遊戲。
							</p>
							<p>*年齡:8歲以上</p>
							<p>*遊戲人數:3-10人</p>
							<p>*遊戲時間:30分鐘</p>

							<h1>第4名:誰是牛頭王TAKE 6!</h1>
							<img src="jomor_html/img/blogimg/blog_title02-2.png" class="blog_gameimg">
							<p>誰是牛頭王是一款悠閒但是卻刺激緊張的遊戲，有時候想害人反而會害到自己。</p>
							<p>遊戲目標：盡可能不要得到牌面上任何牌，因為牌上的每個牛頭都是-1分。</p>
							<p>每位玩家先會獲得10張手牌，然後從牌堆中翻出4張牌當作是起始檯面(起始牌列)。之後遊戲開始，玩家必須同時從10張手牌中打出一張牌，並依照遊戲規則將牌接到桌面上4列牌陣中的一列(看打出的牌與檯面上的牌數字大的往後面接)，若你該回合打出的牌成為某一牌列中的第6張，那麼就必須將前面的5張牌拿起(第6張將成為新一列牌頭)，而每一張牌上會有數量不等的牛頭。</p>
							<p>遊戲結束時，每位玩家數自己拿到幾顆牛頭，而得到最少牛頭的玩家就獲勝。這是一個會讓你非常有緊張感而且非常歡樂的派對遊戲，越多人玩越好玩，會充滿歡笑聲。</p>
							<p>*年齡: 10歲以上</p>
							<p>*遊戲人數: 2-10人</p>
							<p>*遊戲時間: 30分鐘</p>

							<h1>第3名:閃靈快手GEISTESBLITZ</h1>
							<img src="jomor_html/img/blogimg/blog_title02-3.png" class="blog_gameimg">
							<p>閃靈快手是一款比速度跟準確的反應類桌遊，沒有思考完全會很容易抓錯物品。</p>
							<p>遊戲目標：抓取正確物品，獲得最多牌。</p>
							<p>快、狠、準，是閃靈快手中最重要的三個勝利因素。反應越快還有抓對物品，才會獲得勝利。雖然要用腦但玩起來非常輕鬆而且也很刺激。每從牌堆中翻一張牌就要搶奪一次物品，但是又不能亂抓，搶到正確的物品就能拿到當回合的牌(抓到正確的物品可以獲得一張牌，但抓錯牌要將自己的牌給對的人)。每次一翻牌就要大聲尖叫然後與朋友們爭奪物品。這款桌遊是考驗辨識正確物品的速度、還有誰的反應最快。</p>
							<p>是一款非常簡單而且很好上手的派對桌遊。除了適合桌遊新手入門之外，也很適合朋友之間互相比較誰的反應力比較快。遊戲結束時誰有最多的牌就算勝利。</p>
							<p>*年齡: 8歲以上</p>
							<p>*遊戲人數: 2-8人</p>
							<p>*遊戲時間: 20-30分鐘</p>

							<h1>第2名:妙語說書人DIXIT</h1>
							<img src="jomor_html/img/blogimg/blog_title02-4.png" class="blog_gameimg">
							<p>妙語說書人就是要大家說故事，讓其他人來猜猜看哪張圖代表你說的這個故事。</p>
							<p>遊戲目標：分數最高的說書人獲勝。</p>
							<p>當前回合行動的玩家(說書人)要從手中挑選1張牌，並將牌面朝下的放在自己面前，並描述這張牌。但是描述要讓其他人有辦法聯想到，但也不能太容易讓所有人都知道。而其他玩家也要各自從手中挑選1張牌與主講人描述類似的圖案，進行誘導的動作，也是面朝下放於說書人的面前。之後進行洗牌跟投票(不能投自己出的)。</p>
							<p>在說故事與誤導別人的過程中，考驗朋友之間的連結與默契，再加入一點誤導與心機。有些事情是只有自己跟朋友才會知道的小趣事，去猜測對方的想法，過程中會有很多歡笑。</p>
							<p>*年齡: 8歲以上</p>
							<p>*遊戲人數: 3-6人</p>
							<p>*遊戲時間: 30分鐘</p>

							<h1>第1名:德國心臟病HALLI GALLI</h1>
							<img src="jomor_html/img/blogimg/blog_title02-5.png" class="blog_gameimg">
							<p>德國心臟病是一款考驗反應的遊戲。比撲克牌的心臟病還要更刺激的派對遊戲。</p>
							<p>遊戲目標：誰的牌最多誰就勝利，沒牌的則被淘汰。</p>
							<p>誰的反應最快正確的按鈴，就可贏的場面上的牌。而出到手上沒牌的人就出局了，看誰是撐到最後的贏家。玩家依序翻開一張卡片放在自己面前，只要牌面上翻開的紙牌中有一瞬間共出現5個相同水果的時候，每位玩家就要搶按中央的鈴，最快按到鈴的玩家就可以把牌面上所有的卡片家到自己手中(按錯鈴則須把自己一張手牌給各其他所有玩家)。</p>
							<p>而手牌用完的玩家就出局，誰能堅持到最後的玩家就獲勝。這是一款挑戰反應速度刺激緊張的派對遊戲。</p>
							<p>*年齡: 6歲以上</p>
							<p>*遊戲人數: 2-6人</p>
							<p>*遊戲時間: 15分鐘</p>
